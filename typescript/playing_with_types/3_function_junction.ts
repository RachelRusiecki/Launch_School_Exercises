// Write a function greet with two parameters, name (string) and age (number). It should return a greeting: "Hello, your name is [name], and you are [age] years old."

// Expected outcome:

// A string combining the name and age.

function greet(name: string, age: number) {
  return `Hello, your name is ${name}, and you are ${age} years old.`;
}

console.log(greet('Rachel', 31));
